    private static void runCommand() {
    
        try {           
            
            process = Runtime.getRuntime().exec(command);
                        
        } catch (IOException ex) {
            Logger.getLogger(Heal.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
